export function getProductBySlug(slug) {
    return PRODUCTS.find((p) => p.slug === slug);
}

export function getRelatedProducts(product, count = 3) {
    return PRODUCTS.filter((p) => p.category === product.category && p.id !== product.id).slice(0, count);
}

export function getFeatured(count = 4) {
    return PRODUCTS.filter((p) => p.featured).slice(0, count);
}

export function getBestSellers(count = 8) {
    return PRODUCTS.filter((p) => p.bestSeller).slice(0, count);
}

export function getNewArrivals(count = 4) {
    return PRODUCTS.filter((p) => p.newArrival).slice(0, count);
}

export function getDeals(count = 8) {
    return PRODUCTS.filter((p) => typeof p.oldPrice === 'number' && p.oldPrice > p.price).slice(0, count);
}
